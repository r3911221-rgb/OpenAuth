package io.openauth.authenticator.core.crypto.keystore

@JvmInline
value class KeyAlias private constructor(val value: String) {
    init {
        require(value.isValid()) { "Invalid keystore alias" }
    }

    override fun toString(): String = value

    companion object {
        val MASTER_KEY = KeyAlias("openauth_master_key_v1")
        val BIOMETRIC_KEY = KeyAlias("openauth_bio_key_v1")

        private const val MAX_LENGTH = 64
        private val ALIAS_PATTERN = Regex("^[a-zA-Z0-9._-]+$")

        private fun String.isValid(): Boolean {
            return isNotBlank() && length <= MAX_LENGTH && matches(ALIAS_PATTERN)
        }
    }
}