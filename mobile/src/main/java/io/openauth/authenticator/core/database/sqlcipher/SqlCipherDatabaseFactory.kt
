package io.openauth.authenticator.core.database.sqlcipher

import android.content.Context
import androidx.room.Room
import io.openauth.authenticator.core.database.AppDatabase
import io.openauth.authenticator.core.database.migration.DatabaseMigrations
import net.zetetic.database.sqlcipher.SupportOpenHelperFactory
import javax.inject.Inject
import javax.inject.Singleton

@Singleton
class SqlCipherDatabaseFactory @Inject constructor(
    private val passphraseProvider: PassphraseProvider
) {
    fun create(context: Context): AppDatabase {
        System.loadLibrary("sqlcipher")

        val factory = SupportOpenHelperFactory(
            passphraseProvider.getPassphrase()
        )

        return Room.databaseBuilder(
            context,
            AppDatabase::class.java,
            AppDatabase.NAME
        )
        .openHelperFactory(factory)
        .addMigrations(*DatabaseMigrations.ALL)
        .build()
    }
}