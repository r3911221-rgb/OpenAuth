package io.openauth.authenticator.core.crypto.util

private const val HEX = "0123456789abcdef"

fun ByteArray.toHex(): String {
    return buildString(size * 2) {
        for (byte in this@toHex) {
            val value = byte.toInt() and 0xFF
            append(HEX[value ushr 4])
            append(HEX[value and 0x0F])
        }
    }
}

fun String.hexToByteArray(): ByteArray {
    val value = trim()
    require(value.length % 2 == 0) { "Hex length must be even" }

    val result = ByteArray(value.length / 2)

    for (i in result.indices) {
        val index = i * 2
        val high = Character.digit(value[index], 16)
        val low = Character.digit(value[index + 1], 16)

        require(high >= 0 && low >= 0) { "Invalid hex character" }

        result[i] = ((high shl 4) or low).toByte()
    }

    return result
}