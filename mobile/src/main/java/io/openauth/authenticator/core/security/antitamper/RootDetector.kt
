package io.openauth.authenticator.core.security.antitamper

import android.os.Build
import java.io.File
import javax.inject.Inject
import javax.inject.Singleton

@Singleton
class RootDetector @Inject constructor() {

    private val suspiciousPaths = listOf(
        "/system/bin/su",
        "/system/xbin/su",
        "/sbin/su",
        "/system/app/Superuser.apk",
        "/system/app/SuperSU.apk",
        "/data/adb/magisk",
        "/data/adb/magisk.img",
        "/su/bin/su"
    )

    fun isRooted(): Boolean {
        return hasSuBinary() || hasDangerousFiles() || hasTestKeys()
    }

    private fun hasSuBinary(): Boolean {
        return suspiciousPaths.any { File(it).exists() }
    }

    private fun hasDangerousFiles(): Boolean {
        return File("/data/adb").exists()
    }

    private fun hasTestKeys(): Boolean {
        return Build.TAGS?.contains("test-keys") == true
    }
}