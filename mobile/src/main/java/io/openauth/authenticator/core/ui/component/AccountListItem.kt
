package io.openauth.authenticator.core.ui.component

import androidx.compose.foundation.clickable
import androidx.compose.foundation.layout.*
import androidx.compose.material3.MaterialTheme
import androidx.compose.material3.Text
import androidx.compose.runtime.Composable
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.text.font.FontWeight
import androidx.compose.ui.text.style.TextOverflow
import io.openauth.authenticator.core.model.Account
import io.openauth.authenticator.core.ui.theme.Dimens

@Composable
fun AccountListItem(
    account: Account,
    code: String,
    remainingFraction: Float,
    onCopy: () -> Unit,
    onClick: () -> Unit,
    modifier: Modifier = Modifier,
) {
    Row(
        modifier = modifier
            .fillMaxWidth()
            .clickable(onClick = onClick)
            .padding(
                horizontal = Dimens.ScreenPaddingH,
                vertical = Dimens.SpacingM,
            ),
        verticalAlignment = Alignment.CenterVertically,
    ) {
        IconAvatar(
            issuer = account.issuer ?: account.accountName,
            size = Dimens.AvatarSizeM,
        )

        Spacer(modifier = Modifier.width(Dimens.SpacingM))

        Column(
            modifier = Modifier.weight(1f),
        ) {
            Text(
                text = account.issuer ?: account.accountName,
                style = MaterialTheme.typography.titleMedium,
                fontWeight = FontWeight.SemiBold,
                maxLines = 1,
                overflow = TextOverflow.Ellipsis,
            )
            if (account.issuer != null) {
                Text(
                    text = account.accountName,
                    style = MaterialTheme.typography.bodyMedium,
                    color = MaterialTheme.colorScheme.onSurfaceVariant,
                    maxLines = 1,
                    overflow = TextOverflow.Ellipsis,
                )
            }
        }

        Spacer(modifier = Modifier.width(Dimens.SpacingS))

        OtpCodeChip(
            code = code,
            remainingFraction = remainingFraction,
            onCopy = onCopy,
        )
    }
}