package io.openauth.authenticator.core.crypto.backup

import io.openauth.authenticator.core.crypto.cipher.CipherProvider
import io.openauth.authenticator.core.crypto.di.DefaultKdf
import io.openauth.authenticator.core.crypto.kdf.KeyDerivationFunction
import io.openauth.authenticator.core.crypto.model.CryptoConstants
import io.openauth.authenticator.core.crypto.random.SecureRandomProvider
import javax.inject.Inject

class VaultExporter @Inject constructor(
    private val cipher: CipherProvider,
    @DefaultKdf private val kdf: KeyDerivationFunction,
    private val random: SecureRandomProvider
) {
    fun export(plaintext: ByteArray, password: CharArray): BackupCipherSpec {
        require(plaintext.isNotEmpty()) { "Plaintext cannot be empty" }

        val salt = random.nextBytes(CryptoConstants.KDF_SALT_SIZE_BYTES)
        val key = kdf.derive(password, salt, CryptoConstants.AES_KEY_SIZE_BYTES)

        return try {
            val result = cipher.encrypt(key, plaintext)
            BackupCipherSpec(
                BackupFormatVersion.CURRENT,
                kdf.algorithm,
                salt,
                result
            )
        } finally {
            key.fill(0)
        }
    }
}