package io.openauth.authenticator.core.crypto.otp

@JvmInline
value class OtpDigits(
    val value: Int
) {
    init {
        require(value in SUPPORTED) {
            "OTP digits must be one of $SUPPORTED"
        }
    }

    companion object {
        private val SUPPORTED = setOf(6, 7, 8)
        val DEFAULT = OtpDigits(6)
    }
}